class  prog1c
{
	public  static  void  main(String[]  a)
	{
		System . out . println(25);  //  println(int)  prints  25
	    System . out . println(10.8);  //  println(double)  prints  10.8
	    System . out . println('g');  //  println(char)  prints  'g'
	    System . out . println(true);  //  println(boolean)  prints  true
	    System . out . println("Hyd");  //  println(String)  prints  "Hyd"
	}
}


