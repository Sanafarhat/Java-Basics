// How  many  lines  of  output ? 
class  prog3b
{
	public  static  void  main(String[]  args)
	{
		System . out . print("Hyd"); //  Hyd  <same  line>
		System . out . print("Sec"); //  Sec  <same  line>
		System . out . print("Cyb"); //  Cyb  <same  line>
		System . out . println(); //   <next  line>  
		System . out . println("Hyd"); //  Hyd  <next  line>  
		System . out . println("Sec"); //  Sec  <next  line>  
		System . out . println("Cyb"); //  Cyb  <next  line>  
	}
}

